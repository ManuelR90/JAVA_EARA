public class Parte3{

    public static void main(String[] args) {
        //-----Operadores relaciones-----
        //booleano true o false (T o F)
        //(=) asignacion ejemplo: int numero = 10;
        
        // (==) igualdad comparacion
        // (!=) distinto
        // (<) menor que
        // (<=) menor o igual que
        // (>) mayor que
        // (>=) mayor o igual que
        
        int numero1 = 3;
        int numero2 = 5;
        
        System.out.println( (numero1==numero2));
        System.out.println( (numero1!=numero2));
        System.out.println( (numero1<numero2));
        System.out.println( (numero1<=numero2));
        System.out.println( (numero1>numero2));
        System.out.println( (numero1>=numero2));
    }
    
}
