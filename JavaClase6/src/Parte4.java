public class Parte4 {
    public static void main(String [] args){
        // (&&) Operador AND: y
               
       int a = 10, b = 10;
       int C = 85, D = 115;
       
       System.out.println((a == b) && (C > D));
       
       // (||) Operador OR: o
        a = 10;
        b = 10;
        C = 85;
        D = 115;
       
       System.out.println((a == b) || (C > D));
       
        // (!) Operador NOT: no
        a = 10;
        b = 10;
        C = 85;
        D = 115;
       
       System.out.println((a == b) && !(C > D));
        
    }
}
