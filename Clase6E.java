public class Clase6E {
    public static void main(String [] args){
        /**
         * (+=) Suma y asigna
         * (-=) Resta y asigna
         * (*=) Multiplica y asigna
         * (/=) Divide y asigna
         * (%=) Modulo y asigna
         */
        
        System.out.println("\nSuma y asgina");
        int var = 1;
        System.out.println(var);
        var+=3; //var = var + 3
        System.out.println(var);
        
        System.out.println("\nResta y asigna");
        int var2 = 1;
        System.out.println(var2);
        var2-=3; //var = var - 3
        System.out.println(var2);
        
        System.out.println("\nMultiplica y asigna");
        int var3 = 9;
        System.out.println(var3);
        var3*=3; //var = var * 3
        System.out.println(var3);
        
        System.out.println("\nDivide y asigna");
        int var4 = 1;
        System.out.println(var4);
        var4/=3; //var = var / 3
        System.out.println(var4);
        
        System.out.println("\nModulo o Residuo y asigna");
        int var5 = 9;
        System.out.println(var5);
        var5%=3; //var = var / 3
        System.out.println(var5);
        
    }
    
}
